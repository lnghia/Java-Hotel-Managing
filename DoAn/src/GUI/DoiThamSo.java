    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        
        int response = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa thông tin? ", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION)
        {
            try{
                busThamSo.updateThamSo(jTextField1.getText(), jTextField3.getText());
                JOptionPane.showMessageDialog(null, "Sửa thông tin thành công!");
                getTyLePhuThu();

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }finally{
                jTextField1.setText("");
                jTextField3.setText("");
                jTextField3.setEditable(false);
            }
        } else{}
        
    }//GEN-LAST:event_jButton12ActionPerformed

    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        int response = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin? ", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (response == JOptionPane.YES_OPTION)
        {
            try{
                busThamSo.deleteThamSo(jTextField1.getText());
                JOptionPane.showMessageDialog(null, "Xóa thành công!");
                getTyLePhuThu();

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }finally
            {
                jTextField1.setText("");
                jTextField3.setText("");
                jTextField3.setEditable(false);
            }
        }else {}
    }//GEN-LAST:event_jButton13ActionPerformed

