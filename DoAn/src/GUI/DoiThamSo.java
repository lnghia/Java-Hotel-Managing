    public void getTyLePhuThu()
    {
        ArrayList<DTO_THAMSO> arr = new ArrayList<DTO_THAMSO>();
        arr = busThamSo.getThamSo();
        DTO_THAMSO dtoThamSo = new DTO_THAMSO();
        String[] header = { "STT", "Tên tham số", "Giá trị"};
        DefaultTableModel dtm = new DefaultTableModel(header, 0);
        for (int i = 0; i < arr.size(); i++) {
            dtoThamSo = arr.get(i);
            int stt = i +1;
            String tenthamso = dtoThamSo.getTENTHAMSO();
            float giatri = dtoThamSo.getGIATRI();
            Object[] row = {stt, tenthamso, giatri};
            dtm.addRow(row);
        }
        jTable1.setModel(dtm);
    }
 
    
    

