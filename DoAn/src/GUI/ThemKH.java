        tfDiaChi.setText(tbKhachHang.getValueAt(index, 6).toString());
        tfSDT.setText(tbKhachHang.getValueAt(index, 7).toString());
        tfEmail.setText(tbKhachHang.getValueAt(index, 8).toString());        
        CMNDcu= tfCMND.getText();
