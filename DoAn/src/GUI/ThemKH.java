    private void tfCMNDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCMNDKeyPressed
        // TODO add your handling code here:
        if ((evt.getKeyChar() >= '0' && (evt.getKeyChar()) <= '9' &&tfCMND.getText().length()<9)||evt.getKeyChar()==KeyEvent.VK_BACK_SPACE || evt.getKeyChar() ==KeyEvent.VK_DELETE)
        {
            if (evt.getKeyChar()==KeyEvent.VK_BACK_SPACE  || evt.getKeyChar() ==KeyEvent.VK_DELETE)
            {
                String S = tfCMND.getText();
                tfCMND.setText(S.substring(0, S.length()-1));
            }
               tfCMND.setEditable(true);
        } else 
        {
            tfCMND.setEditable(false); 
            JOptionPane.showMessageDialog(null,"Số CMND tối đa là 9 chữ số ", "Thông báo",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_tfCMNDKeyPressed

