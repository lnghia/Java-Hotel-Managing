    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
       // if ()
       if (rbKhac.isSelected())
           GioiTinh="Khác";
       if (rbNam.isSelected())
           GioiTinh="Nam";
       if (rbNu.isSelected())
           GioiTinh="Nữ";
       NgaySinh = String.format("%1$td-%1$tm-%1$tY", dcNgaySinh.getDate());  
       tfMaKH.setText(tfCMND.getText());
       if (DSKhachHang.size()==bus_ThemKH.SoKhachToiDa())
           JOptionPane.showMessageDialog(null, "Vượt quá số khách tối đa.", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
       else
       if (tfMaKH.getText().length()>0&&tfHoTen.getText().length()>0&&tfCMND.getText().length()>0&&tfEmail.getText().length()>0&&tfSDT.getText().length()>0&&tfDiaChi.getText().length()>0&&cbLoaiKH.getSelectedItem().toString().length()>0&&NgaySinh.length()>0&& GioiTinh.length()>0)
       {
           DTO_KhachHang dto = new DTO_KhachHang(tfMaKH.getText(), cbLoaiKH.getSelectedItem().toString(), tfHoTen.getText(), GioiTinh, NgaySinh, tfCMND.getText(), tfDiaChi.getText(),tfSDT.getText(), tfEmail.getText(),  true);           
           if (Exist)
           {
               if (check(tfCMND.getText()))
               {
                   DSKhachHang.add(dto);
                   ThemKH();
                   Exist=false;
               }
               else
                   JOptionPane.showMessageDialog(null, "Đã có trong danh sách khách thuê rồi", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
           }
           else
           {
            
            int rs = bus_ThemKH.Add(dto);
           if (rs>0)
           {
               DSKhachHang.add(dto);
               ThemKH();
               JOptionPane.showMessageDialog(null, "Thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
           }
           else
               JOptionPane.showMessageDialog(null, "Không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
           }
           
       }
       
        Reset();
    }//GEN-LAST:event_btThemActionPerformed
    public void ThemKH()
    {
        dtm = new DefaultTableModel(Header,0);
        for (int i=0; i<DSKhachHang.size();i++)
        {
            Vector data = new Vector();
        data.add(DSKhachHang.get(i).getMaKhachHang());
        data.add(DSKhachHang.get(i).getMaLoaiKhachHang());
        data.add(DSKhachHang.get(i).getHoTen()); 
        data.add(DSKhachHang.get(i).getGioiTinh());
        data.add(DSKhachHang.get(i).getNgaySinh());
        data.add(DSKhachHang.get(i).getCMND());
        data.add(DSKhachHang.get(i).getDiaChi());
        data.add(DSKhachHang.get(i).getSDT());
        data.add(DSKhachHang.get(i).getEmail());
        dtm.addRow(data);
        }
        tbKhachHang.setModel(dtm);
    }
    public boolean check(String CMND) 
    {
        for (int i=0; i<DSKhachHang.size();i++)
        {
            if (DSKhachHang.get(i).getCMND().equals(CMND))
                return false;
        }
        return true;
    }
