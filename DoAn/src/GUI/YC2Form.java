    public void GetDSKhachHang1()
    {
        String [] Header ={"Mã loại khách", "Họ tên","CMND", "Địa chỉ"}; 
        DefaultTableModel dtm = new DefaultTableModel(Header,0);
        dtm.setRowCount(0);
        DTO_KhachHang dto = new DTO_KhachHang();
        for (int i=0; i<ThemKH.DSKhachHang.size();i++)
        {
            dto=ThemKH.DSKhachHang.get(i);
            String maLoaiKH = dto.getMaLoaiKhachHang();
            TyLePhuThu+=bus.GetHeSoPhuThu(dto.getMaLoaiKhachHang());
            String hoten = dto.getHoTen();
            String CMND = dto.getCMND();
            String DiaChi = dto.getDiaChi();
            Object [] row = {maLoaiKH, hoten, CMND,DiaChi};
            dtm.addRow(row);
        }
            tbKhachThue.setModel(dtm);
            if (ThemKH.DSKhachHang.size()>bus.GetSoKhachKhongPhuThu())
                TyLePhuThu+=(ThemKH.DSKhachHang.size()-bus.GetSoKhachKhongPhuThu())*bus.GetTiLePhuThuTheoSoKhach();
    }
   
    
