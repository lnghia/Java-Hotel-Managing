    public void loadData()
    {        
        String [] Header = {"Mã phiếu thuê", "Mã phòng","Hình thức thuê", "Giờ bắt đầu thuê", "Ngày bắt đầu thuê", "Số khách", "Đơn giá tiêu chuẩn", "Đơn giá thuê "};
        DefaultTableModel dtm = new DefaultTableModel(Header, 0);
        ArrayList <DTO_PhieuThuePhong> arr = new ArrayList<DTO_PhieuThuePhong>();
        BUS_YC2 bu = new BUS_YC2();
        arr= bu.getData();
        dtm.setRowCount(0);
        DTO_PhieuThuePhong dto = new DTO_PhieuThuePhong();
        for (int i=0; i<arr.size();i++)
        {
            dto=arr.get(i);
            String maptp = dto.getMaPTP();
            String maphong = dto.getMaPhong();
            String mahinhthuc = dto.getMaHinhThuc();
            String giothue = dto.getGioBatDauThue();
            String ngaythue = dto.getNgayBatDauThue();
            int sk = dto.getSoKhach();
            float dgt = dto.getDonGiaThue();
            float dgtc = dto.getDonGiaTieuChuan();
            Object [] row = {maptp,maphong,mahinhthuc,giothue,ngaythue,sk,dgt,dgtc};
            dtm.addRow(row);
        }
            tbDanhSach.setModel(dtm);
    }
