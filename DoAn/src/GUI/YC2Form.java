    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        
       if (tfMaPTP.getText().length()>0 && cbMaPhong.getSelectedItem().toString().length()>0 && cbHinhThucThue.getSelectedItem().toString().length()>0 && tfGioBatDauThue.getText().length()>0 && tfNgayThue.getText().length()>0&& tfSoKhach.getText().length()>0&& tfDonGiaThue.getText().length()>0 && tfDonGiaTieuChuan.getText().length()>0)
        {
            int sk=Integer.parseInt(tfSoKhach.getText());
            float dgt=Float.parseFloat(tfDonGiaThue.getText());
            float dgtc=Float.parseFloat(tfDonGiaTieuChuan.getText());
            
            DTO_PhieuThuePhong dto = new DTO_PhieuThuePhong(tfMaPTP.getText(), cbMaPhong.getSelectedItem().toString(), cbHinhThucThue.getSelectedItem().toString(), tfGioBatDauThue.getText(), tfNgayThue.getText(),sk, dgt, dgtc, true, false );
            if (bus.checkPhong(cbMaPhong.getSelectedItem().toString()))
            {
                int result = bus.Add(dto);
                if (result>0)
                {
                    ThemCT_PTP();
                    JOptionPane.showMessageDialog(null,"Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            else
                JOptionPane.showMessageDialog(null,"Phòng đang được thuê", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null, "Cần nhập đầy đủ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        loadData();
        int re = bus.UpdatePhong(cbMaPhong.getSelectedItem().toString(), "Đang thuê");
        Reset();
    }//GEN-LAST:event_btThemActionPerformed
    private void ThemCT_PTP()
            {
                int re=0;
        for (int i=0; i<ThemKH.DSKhachHang.size();i++)
        {
            int rs = bus_ThemKH.Add_CT(String.valueOf(bus_ThemKH.MaPTP()), ThemKH.DSKhachHang.get(i).getHoTen(),ThemKH.DSKhachHang.get(i).getMaLoaiKhachHang(), ThemKH.DSKhachHang.get(i).getCMND(),ThemKH.DSKhachHang.get(i).getDiaChi(), true);
            if (rs>0)
                re++;
        }    
        if (re== ThemKH.DSKhachHang.size())
            JOptionPane.showMessageDialog(null, "Thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
           else
               JOptionPane.showMessageDialog(null, "Không thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
    
