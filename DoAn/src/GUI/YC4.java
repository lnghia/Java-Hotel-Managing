    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        NumberFormat fmt = NumberFormat.getCurrencyInstance();
        
        try {
            // TODO add your handling code here:
//        HoaDonBUS.getInstance().generateReceive();
            PhieuThuePhongDTO ptp=PhieuThuePhongDAL.getInstance().getMostRecentPTPByRoomId(getMaPh());
            if(ptp==null){
                JOptionPane.showMessageDialog(this, "Phòng này hiện chưa có phiếu thuê!");
                return;
            }
            SimpleDateFormat df=new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            feedDataTable();
            tienO=YC4DAL.getInstance().getTienO(getMaPh());
            
            if(ptp.getMaHTThue().equals("Thuê theo giờ")){
                Date d1=df.parse(ptp.getNgayBDThue()+ " " +ptp.getGioBDThue());
                long secs=Math.abs((d1.getTime()- new Date().getTime()))/1000;
                int hours=(int) (secs/3600);
                tienO*=hours;
            }
            else{
                Date d=df.parse("dd/MM/yyyy");
                LocalDate tmp=LocalDate.now();
                LocalDate t=d.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

                int time=Period.between(LocalDate.now(), d.toInstant().atZone(ZoneId.systemDefault()).toLocalDate()).getDays();
                tienO*=time;
            }
            
            feedDataTable();
            tienO=YC4DAL.getInstance().getTienO(getMaPh());
            float tongtien=YC4BUS.getInstance().calculateSumMoney(sum, tienO);
            
//            jTextField4.setText(Float.toString(tongtien));
            jTextField4.setText(fmt.format(tongtien));
            
            String loaiPh=PhongDAL.getInstance().getRoomTypeId(getMaPh());
            
            soHd=YC4DAL.getInstance().themHoaDon(tenKhTxt.getText(), jTextField3.getText(), Float.toString(tongtien), loaiPh);
            
            if(soHd>0){
                DateFormat dfm=new SimpleDateFormat("MM/dd/yyyy");
                String toDay=java.time.LocalDate.now().format(DateTimeFormatter.ofPattern("MM/dd/yyyy"));
                String[] tokens=toDay.split("/");
                
                YC4DAL.getInstance().themBaoCao(tokens[0], tokens[2]);
                YC4DAL.getInstance().themCTBC(tokens[0], tokens[2], PhongDAL.getInstance().getRoomTypeId(getMaPh()), Float.toString(tongtien));
                
                
                JOptionPane.showMessageDialog(this, "Thao tác thành công!");  
                YC4DAL.getInstance().updateTrangThaiPhieuThue(getMaPh());
                YC4DAL.getInstance().updateTrangThaiPhong(getMaPh());
                soPh=getMaPh();
            }
            else{
                JOptionPane.showMessageDialog(this, "Thao tác thất bại!");
            }
            
            //JOptionPane.showMessageDialog(this, "Thao tác thất bại!");
        } catch (ParseException ex) {
            Logger.getLogger(YC4.class.getName()).log(Level.SEVERE, null, ex);
        }
        getPhongDangThue();
    }//GEN-LAST:event_jButton11ActionPerformed

