    void getPhongDangThue()
    {
        ArrayList<DTO_Phong> arr = new ArrayList<DTO_Phong>();
        arr = busCapNhatPhong.getPhongDangThue();
        DTO_Phong dtoPhong = new DTO_Phong();
        String[] header = { "STT", "Mã phòng", "Mã loại phòng", "Mô tả", "Ghi chú", "Tình trạng"};
        DefaultTableModel dtm = new DefaultTableModel(header, 0);
        for (int i = 0; i < arr.size(); i++) {
            dtoPhong = arr.get(i);
            int stt = i + 1;
            String maphong = dtoPhong.getMaPhong();
            String maloaiphong = dtoPhong.getMaLoaiPhong();
            String mota = dtoPhong.getMoTa();
            String ghichu = dtoPhong.getGhiChu();
            String tinhtrang = dtoPhong.getTinhTrang();
            Object[] row = {stt, maphong, maloaiphong, mota, ghichu, tinhtrang};
            dtm.addRow(row);
        }
        jTable2.setModel(dtm);
    }
    
    public void feedDataTable() throws ParseException{
        sum=0;
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        NumberFormat fmt = NumberFormat.getCurrencyInstance();
        
        model.setRowCount(0);
        
        if(dvList!=null && !dvList.isEmpty()){
            dvList.clear();
        }
        getDvs();
        
        for(CTSuDungDichVuDTO row : dvList){
            sum+=row.getThanhTien();
            model.addRow(new Object[]{DichVuDAL.getInstance().getTenDV(row.getMaDichVu()), row.getSoLuong(), fmt.format(row.getDonGia()), fmt.format(row.getThanhTien())});
        }
    }

